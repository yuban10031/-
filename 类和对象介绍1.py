class Turtle:
    """关于类的一个简单例子"""
    #属性
    color = 'green'
    weight = 10
    legs = 4
    shell = True
    south = '大嘴'

    
    # 方法
    def climb(self):
        print('我正在很努力的向前爬.......')
    def run(self):
        print('我正在飞快的向前跑........')
    def bite(self):
        print('我咬死你，我咬死你')
    def eat(self):
        print('吃鸡！')
    def sleep(self):
        print('睡觉！')
